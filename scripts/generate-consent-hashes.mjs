#!/usr/bin/env node
// Generates the SQL that replaces the placeholder consent body hashes with
// SHA-256 hashes of the approved consent documents in docs/consents/.
//
// Usage:
//   node scripts/generate-consent-hashes.mjs           # prints SQL to stdout
//   node scripts/generate-consent-hashes.mjs --check   # verifies approval status only
//
// Every consent file must set `status: approved` in its frontmatter before SQL
// is produced. This is deliberate: the hash must bind to language a human owner
// has signed off, never to a draft.

import { createHash } from 'node:crypto'
import { readdirSync, readFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const consentsDir = join(dirname(fileURLToPath(import.meta.url)), '..', 'docs', 'consents')
const checkOnly = process.argv.includes('--check')

const files = readdirSync(consentsDir).filter((name) => name.endsWith('.md')).sort()
if (!files.length) {
  console.error('No consent documents found in docs/consents/.')
  process.exit(1)
}

const drafts = []
const rows = []

for (const name of files) {
  const raw = readFileSync(join(consentsDir, name), 'utf8')
  const frontmatter = /^---\n([\s\S]*?)\n---\n/.exec(raw)
  if (!frontmatter) {
    console.error(`${name}: missing frontmatter block.`)
    process.exit(1)
  }
  const meta = Object.fromEntries(
    frontmatter[1].split('\n').map((line) => {
      const separator = line.indexOf(':')
      return [line.slice(0, separator).trim(), line.slice(separator + 1).trim()]
    }),
  )
  for (const required of ['consent_key', 'version', 'status']) {
    if (!meta[required]) {
      console.error(`${name}: frontmatter is missing "${required}".`)
      process.exit(1)
    }
  }
  if (meta.status !== 'approved') {
    drafts.push(`${name} (status: ${meta.status})`)
    continue
  }
  // Hash the entire file so the frontmatter (key, version, title) is bound too.
  const hash = createHash('sha256').update(raw).digest('hex')
  rows.push({ key: meta.consent_key, version: meta.version, hash, name })
}

if (drafts.length) {
  console.error('Refusing to emit SQL — these consent documents are not approved yet:')
  for (const entry of drafts) console.error(`  - ${entry}`)
  console.error('\nHave the owner review each document, change "status: draft" to')
  console.error('"status: approved", then re-run this script.')
  process.exit(2)
}

if (checkOnly) {
  console.log(`All ${rows.length} consent documents are approved.`)
  process.exit(0)
}

console.log('-- Generated by scripts/generate-consent-hashes.mjs')
console.log(`-- ${new Date().toISOString()} — paste into the Supabase SQL editor.`)
console.log('begin;')
for (const row of rows) {
  console.log(`update public.consent_versions set body_hash = '${row.hash}' where consent_key = '${row.key}' and version = '${row.version}'; -- ${row.name}`)
}
console.log(`select consent_key, version, body_hash from public.consent_versions where body_hash = 'configure-production-hash';`)
console.log('-- The select above must return zero rows; otherwise a key/version was missed.')
console.log('commit;')
